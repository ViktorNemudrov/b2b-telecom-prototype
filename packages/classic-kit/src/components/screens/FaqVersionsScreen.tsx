function formatVersionReleasedAtLabel(versionTag: string): string {
  const commitTimestamp = FAQ_VERSION_COMMIT_TIMESTAMPS[versionTag];
  if (!commitTimestamp) return "без коммита";
  const d = new Date(commitTimestamp);
  return new Intl.DateTimeFormat("ru-RU", {
    day: "numeric",
    month: "long",
    year: "numeric",
    hour: "2-digit",
    minute: "2-digit",
    timeZone: "Europe/Moscow"
  }).format(d);
}

